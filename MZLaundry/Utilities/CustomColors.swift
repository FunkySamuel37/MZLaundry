import UIKit

extension UIColor {
    
    class func getLightGrayBorderColor()-> UIColor{
        return UIColor(red: 221.0/255.0, green: 221.0/255.0, blue: 221.0/255.0, alpha: 1.0)
    }
    
    class func getCustOrangeColor()-> UIColor{
        return UIColor(red: 255.0/255.0, green: 77.0/255.0, blue: 15.0/255.0, alpha: 1.0)
    }
    
    class func getDarkGrayFontColor()-> UIColor{
        return UIColor(red: 58.0/255.0, green: 58.0/255.0, blue: 58.0/255.0, alpha: 1.0)
    }
    
    class func getMainBlueColor()-> UIColor{
        return UIColor(red: 40.0/255.0, green: 204.0/255.0, blue: 252.0/255.0, alpha: 1.0)
    }

}
